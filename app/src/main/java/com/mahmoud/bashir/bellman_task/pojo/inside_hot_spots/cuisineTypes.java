package com.mahmoud.bashir.bellman_task.pojo.inside_hot_spots;

public class cuisineTypes {
    int id;
    String name;

    public cuisineTypes(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public cuisineTypes() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
